                                        <p><strong>Ebene, Mauritius, August 12, 2019:</strong> Azure Power Global
                                            Limited (NYSE: AZRE), a leading independent solar power producer in India,
                                            today announced its consolidated results under United States Generally
                                            Accepted Accounting Principles (“GAAP”) for the fiscal first quarter 2020
                                            ended June 30, 2019.</p>

                                        <p><strong>Fiscal First Quarter 2020 Period Ended June 30, 2019 Operating
                                                Highlights:</strong></p>
                                        <ul>
                                            <li>Net income of INR 90.2 million (US$ 1.3 million) for the quarter ended
                                                June 30, 2019 as compared to INR 29.8 million for quarter ended June 30,
                                                2018.</li>
                                            <li>Operating Megawatts (“MW”) were 1,609 MW, as of June 30, 2019, an
                                                increase of 59% over quarter ended June 30, 2018.</li>
                                            <li>Operating and Committed Megawatts were 3,351 MW, as of quarter ended
                                                June 30, 2019, an increase of 57% over quarter ended June 30, 2018.</li>
                                            <li>Revenue for the quarter ended June 30, 2019 was INR 3,389.3 million (US$
                                                49.2 million), an increase of 40 % over the quarter ended June 30, 2018.
                                            </li>
                                            <li>Adjusted EBITDA for the quarter ended June 30, 2019 was INR 2,447.7
                                                million (US$ 35.5 million), an increase of 25% over the quarter ended
                                                June 30, 2018.</li>
                                        </ul>

                                        <p><strong><em>Key Operating Metrics</em></strong></p>
                                        <p>Electricity generation during the quarter ended June 30, 2019 increased by
                                            299.3 million kWh, or 75%, to 700.1 million kWh, compared to the quarter
                                            ended June 30, 2018. The increase in electricity generation was principally
                                            a result of additional operating capacity during the period.</p>
                                        <p>Total revenue during the quarter ended June 30, 2019 was INR 3,389.3 million
                                            (US$ 49.2 million), up 40% from INR 2,422.5 million during the same period
                                            in 2018. The increase in revenue was primarily driven by the commissioning
                                            of new projects.</p>

                                        <p>Project cost per megawatt operating (megawatt capacity per the power purchase
                                            agreement) consists of costs incurred for one megawatt of new solar power
                                            plant capacity during the reporting period. The project cost per megawatt
                                            operating for the quarter months ended June 30, 2019 decreased by INR 4.1
                                            million (US$ 0.06 million) to INR 40.2 million (US$ 0.58 million) primarily
                                            due to lower costs on account of the reduction in solar module prices for
                                            the projects commissioned during the period partly offset by approximately
                                            US$ 0.05 million per megawatt payment of safe guard duties that the company
                                            expects to recover.</p>

                                        <p>As of June 30, 2019, the Company’s operating and committed megawatts
                                            increased by 1,210 MW to 3,351 MW compared to June 30, 2018 as a result of
                                            obtaining new projects.</p>

