                                        <p>Fiscal First Quarter 2020 Period ended June 30, 2019 Consolidated Financial
                                            Results:</p>

                                        <p><strong><em>Operating Revenues</em></strong></p>
                                        <p>Operating revenue for the quarter ended June 30, 2019 was INR 3,389.3 million
                                            (US$ 49.2 million), an increase of 40% from INR 2,422.5 million over the
                                            same period in 2018. The increase in revenue was driven by the commissioning
                                            of new projects during the period after June 30, 2018 until June 30, 2019.
                                        </p>
                                        <p><strong><em>Cost of Operations (Exclusive of Depreciation and
                                                    Amortization)</em></strong></p>
                                        <p>Cost of operations for the quarter ended June 30, 2019 increased by 36% to
                                            INR 296.9 million (US$ 4.3 million) from INR 218.2 million in the same
                                            period in 2018. The increase was primarily due to higher plant maintenance
                                            cost arising from newly commissioned projects during the period after June
                                            30, 2018 until June 30, 2019.</p>

                                        <p><strong><em>General and Administrative Expenses</em></strong></p>

                                        <p>General and administrative expenses for the quarter ended June 30, 2019
                                            increased by INR 396.0 million (US$ 5.7 million), to INR 644.7 million (US$
                                            9.4 million) compared to the same period in 2018. General and administrative
                                            expenses increased primarily on account of higher payroll costs and one-time
                                            charges of INR 264.4 million (US$ 3.8 million) primarily related to
                                            management transition.</p>

                                        <p><strong><em>Depreciation and Amortization Expenses</em></strong></p>
                                        <p>Depreciation and amortization expenses during the quarter ended June 30, 2019
                                            increased by INR 69.8 million (US$ 1.0 million), or 13%, to INR 623.4
                                            million (US$ 9.0 million) compared to the same period in 2018. The increase
                                            in depreciation and amortization expense reflected the additional
                                            depreciation on new projects commissioned since June 30, 2018, was offset by
                                            a decrease in depreciation expense on account of change in useful life,
                                            which was effective October 1, 2018. For a detailed discussion, please refer
                                            to Note 2(i), to our consolidated financial statements in our Form 20-F for
                                            the year ended March 31, 2019.</p>

                                        <p><strong><em>Interest Expense, Net</em></strong></p>
                                        Interest expense, net during the quarter ended June 30, 2019 increased by INR
                                        486.7 million (US$ 7.1 million), or 45%, to INR 1,560.1 million (US$ 22.6
                                        million) compared to the same period in 2018. Interest expense increased due to
                                        additional loans related to new projects but was partially offset by higher
                                        interest income on investments during the quarter ended June 30, 2019.<p></p>

                                        <p><strong><em>Loss on Foreign Currency Exchange</em></strong></p>
                                        <p>During the quarter ended June 30, 2019, the Company incurred losses of INR
                                            50.3 million (US$ 0.7 million) compared to a loss of INR 204.2 million,
                                            during the quarter ended June 30, 2018, primarily due the expense of hedging
                                            foreign exchange rates.</p>

                                        <p><strong><em>Income Tax Expense</em></strong></p>
                                        <p>The income tax expense increased during the quarter ended June 30, 2019 by
                                            INR 29.2 million (US$ 0.4 million) to INR 123.7 million (US$ 1.8 million),
                                            compared to INR 94.6 million in the same period in 2018, primarily as a
                                            result of higher income from operations.</p>

                                        <p><strong><em>Net Income</em></strong></p>
                                        <p>The net income for the quarter ended June 30, 2019 increased by INR 60.4
                                            million (US$ 0.9 million) to INR 90.2 million (US$ 1.3 million), compared to
                                            a net income of INR 29.8 million for the same period in 2018. The increase
                                            was primarily due to higher revenues achieved during the period.</p>

                                        <p><strong><em>Cash Flow and Working Capital</em></strong></p>
                                        <p>Cash used in operating activities for the quarter ended June 30, 2019 was INR
                                            466.7 million (US$ 6.8 million), INR 283.0 million (US$ 4.1 million)
                                            improvement from the comparable period in 2018, primarily due to an increase
                                            in revenue during the quarter ended June 30, 2019. During the quarter ended
                                            June 30, 2019, additional cash interest paid on loans compared to the same
                                            period in the prior year was INR 358.2 million (US$ 5.2 million). In
                                            addition, cash flow in operating activities is impacted more in the first
                                            and third quarters of the fiscal year primarily related to the semi-annual
                                            payment of interest on green bonds.</p>

                                        <p>Cash used in investing activities for the quarter ended June 30, 2019 was INR
                                            5,666.6 million (US$ 82.2 million), compared to INR 4,196.0 million for the
                                            comparable period in 2018, primarily on account of purchases of property
                                            plant and equipment for new solar projects amounting to INR 5,657.8 million
                                            (US$ 82.1 million).</p>

                                        <p>Cash generated from financing activities was INR 10,742.0 (US$ 155.9 million)
                                            for the quarter ended June 30, 2019, compared to INR 4,251.7 million for the
                                            comparable period in 2018, primarily due to additional debt incurred related
                                            to new solar projects.</p>

                                        <p><strong><em>Liquidity Position</em></strong></p>
                                        <p>As of June 30, 2019, the Company had INR 11,470.7 million (US$ 166.4 million)
                                            of cash, cash equivalents and current investments. The Company had undrawn
                                            project debt commitments of INR 10,876.2 million (US$ 157.8 million) as of
                                            June 30, 2019.</p>

                                        <p><strong><em>Adjusted EBITDA</em></strong></p>
                                        <p>Adjusted EBITDA was INR 2,447.7 million (US$ 35.5 million) for the quarter
                                            ended Jun 30, 2019, compared to INR 1,955.7 million for the quarter ended
                                            June 30, 2018. The increase was primarily due to the increase in revenue
                                            during the quarter ended June 30, 2019.</p>

