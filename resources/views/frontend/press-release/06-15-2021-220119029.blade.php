                    <div class="bw-release-story" itemprop="articleBody">
                        <p><strong>Ebene, June 15, 2021:</strong></p>
                        <p>Azure Power Global Limited (NYSE: AZRE), a leading independent renewable power producer in
                            India, today announced its consolidated results under United States Generally Accepted
                            Accounting Principles (“GAAP”) for the fiscal fourth quarter
                            2021, period ended March 31, 2021.</p>

                        <p><strong>Fiscal Fourth Quarter 2021 Period Ended March 31, 2021 Operating Highlights:</strong>
                        </p>
                        <ul>
                            <li>Megawatts (“MW”) Operating* were 1,990 MWs, as of March 31, 2021, an increase of 20%
                                over March 31, 2020. Operating, Contracted &amp; Awarded MW* were 6,955 MWs, as of March
                                31, 2021. Contracted &amp; Awarded megawatts include 4,000 MWs for which we have
                                received Letters of Award (“LOA”) but the Power Purchase Agreements (“PPAs”) have not
                                yet been signed.</li>

                            <li>Operating revenues for the quarter ended March 31, 2021 were INR 4,271 million (US$ 58.4
                                million), an increase of 16% over the quarter ended March 31, 2020. We estimate that our
                                revenues were negatively impacted by approximately INR 50 million (US$ 0.7 million) on
                                account of lower insolation, as compared to our forecast for the quarter.</li>

                            <li>Net loss for the quarter ended March 31, 2021 was INR 2,791 million (US$ 38.1 million).
                                During the quarter, our results were negatively impacted by impairment loss on assets of
                                INR 3,255 million (US$ 44.5 million), partially offset by reversal in stock appreciation
                                rights (SARs) expense of INR 560 million (US$ 7.7 million). Refer to the detailed
                                explanation in the ‘Impairment loss’ and ‘Stock Appreciation Rights expense’ section of
                                the commentary below.</li>

                            <li>Adjusted EBITDA for the quarter ended March 31, 2021 was INR 3,799 million (US$ 52.0
                                million), an increase of 44% over the quarter ended March 31, 2020.</li>

                            <li>Non-GAAP Cash Flow to Equity (“CFe”) from Operating Assets for the quarter ended March
                                31, 2021 was INR 1,743 million (US$ 23.9 million), an increase of 55% over the quarter
                                ended March 31, 2020.</li>
                        </ul>

                        <p>*Megawatts Operating and Megawatt Operating, Contracted &amp; Awarded exclude the Rooftop
                            portfolio, for which we entered into an agreement to sell subsequent to fiscal year ended
                            March 31, 2021. We excluded 160 MWs from our Operating, Contracted &amp; Awarded portfolio
                            as of March 31, 2021, and the prior comparative period. We excluded 153 MWs from our
                            Operating portfolio as of March 31, 2021, and 144 MWs from prior comparable period. The
                            exclusion from prior period is for the purpose of comparison with current year.</p>

                        <p><strong><em>Key Operating Metrics</em></strong></p>

                        <p>Electricity generation during the quarter and fiscal year ended March 31, 2021 was 1,058
                            million kWh and 3,495 million kWh, respectively, an increase of 190 million kWh or 21.9 %,
                            over the quarter ended March 31, 2020, and an increase of 626 million kWh, or 21.8%, over
                            the year ended March 31, 2020. The increase in electricity generation was principally a
                            result of an additional 335 MWs of AC (506 MWs DC) operating capacity, including our Rooftop
                            portfolio commissioned since March 31, 2020. Our Plant Load Factor (“PLF”) for the quarter
                            and the fiscal year ended March 31, 2021, was 23.4 % and 20.9% respectively, compared to
                            22.3% and 19.5%, respectively, for the same comparable periods in 2020, which increased
                            principally due to the addition of AC and DC capacity and improved performance by our
                            plants.</p>

                        <p>We commissioned 156 MWs AC (240 MWs DC) during the three months ended March 31, 2021, and 335
                            MWs AC (506 MWs DC) during the fiscal year ended March 31, 2021, including the 6 MWs and 10
                            MWs (both AC &amp; DC) that we commissioned for our rooftop portfolio for the quarter and
                            fiscal year ended March 31, 2021, respectively.</p>

                        <p>Project cost per megawatt operating (megawatt capacity per the PPA or AC) consists of costs
                            incurred for one megawatt of new solar power plant capacity during the reporting period. The
                            project cost per megawatt (DC) operating for the fiscal year ended March 31, 2021 decreased
                            by INR 6.7 million (US$ 0.09 million), or 19%, to INR 28.8 million (US$ 0.39 million)
                            primarily due to lower costs on account of the reduction in solar module prices for the
                            projects commissioned during the period. The project cost per megawatt (AC) operating for
                            the fiscal year ended March 31, 2021 was INR 42.9 million (US$ 0.59 million), compared to
                            INR 48.9 million, for the year ended March 31, 2020, on account of a reduction in solar
                            module prices. Excluding the impact of safeguard duties, the DC and the AC costs per
                            megawatt for the fiscal year ended March 31, 2021 would have been lower by approximately INR
                            2.7
                            million (US$ 0.04 million) and INR 2.8 million (US$ 0.04 million), respectively, and for the
                            prior fiscal year ended March 31, 2020, the DC and the AC costs per megawatt would have been
                            lower by approximately INR 2.9 million and INR 4.9 million, respectively.</p>
                        <p>As of March 31, 2021, our Operating, Contracted &amp; Awarded megawatts were 6,955 MWs. There
                            was no change compared to prior comparable period, other than to reflect the disposal of the
                            rooftop portfolio. Contracted &amp; Awarded megawatts include 4,000 MWs for which we have
                            received LOAs but the PPAs have not yet been signed. The Solar Energy Corporation of India
                            (“SECI”) has informed us that so far there has not been adequate response from the state
                            electricity distribution companies (“DISCOMs”) for SECI to be able to sign the Power Sale
                            Agreement (“PSA”) at this stage even though we have a LOA.</p>

                        <p>SECI has mentioned that they will be unable to sign PPAs until PSAs have been signed, and
                            they have committed to inform Azure Power of developments in their efforts with the DISCOMS.
                            Capital costs, interest rates and foreign exchange rates have improved since Azure Power won
                            the 4 GW auction in December 2019 which have resulted in lower tariffs in other recent SECI
                            auctions. We expect these savings likely will be passed on to state electricity distribution
                            companies (DISCOMS). We expect a tariff markdown from the price achieved in the auction,
                            which will facilitate signing of PSAs. We will continue our discussions with SECI towards
                            signing PPAs in respect of the 4GW tender and believe the PPAs to be signed in tranches over
                            a period of time.</p>

                        <p><strong><em>Megawatts Operating and Megawatts Contracted &amp; Awarded</em></strong></p>

                        <p>We measure the rated capacity of our plants in megawatts. Rated capacity is the expected
                            maximum output that a solar power plant can produce without exceeding its design limits. We
                            believe that tracking the growth in aggregate megawatt rated capacity is a
                            measure of the growth rate of our business.</p>

                        <p>“Megawatts Operating” represents the aggregate cumulative megawatt rated capacity of solar
                            power plants that are commissioned and operational as of the reporting date.</p>

                        <p>“Megawatts Contracted &amp; Awarded” represents the aggregate megawatt rated capacity of
                            solar power plants pursuant to customer PPAs signed, allotted or won in an auction but not
                            commissioned and operational as of the reporting date.</p>
