    </p><p><b>Third Quarter Period ended December 31, 2017 Consolidated Financial
      Results: </b><br><i><b>Operating Revenue </b></i><br>Operating
      revenue in the quarter ended December 31, 2017 was INR 1,739.9 million
      (<money>US$ 27.3 million</money>), an increase of 83% from INR 948.8 million over the
      same period in 2016. The increase in revenue was driven by the
      commissioning of new projects.
    </p><p><i><b>Cost of Operations </b></i><br>Cost of operations in the quarter
      ended December 31, 2017 increased by 90% to INR 158.4 million (<money>US$ 2.5
      million</money>) from INR 83.2 million in the same period in 2016. The increase
      was primarily due to plant maintenance cost for newly commissioned
      projects which was partially offset by the implementation of improved
      O&amp;M methods which improved plant productivity. This includes INR 8.7
      million (<money>US$ 0.1 million</money>) of non-cash expense, which pertains to the
      amortisation of lease expense.
    </p><p><i><b>General and Administrative Expenses </b></i><br>General and
      administrative expenses for the quarter ended December 31, 2017
      increased by INR 185.3 million (<money>US$ 2.9 million</money>), to INR 354.5 million
      (<money>US$ 5.6 million</money>) compared to the same period in 2016. However, due to a
      delay by the government in bundling of thermal power with solar power
      production at one of our recently commissioned project, we recorded a
      one-time charge of INR 83.6 million (<money>US$ 1.3 million</money>). Our project
      contract period was extended by the duration of the delay by the
      government.
    </p><p><i><b>Depreciation and Amortization Expenses </b></i><br>Depreciation
      and amortization expenses during the quarter ended December 31, 2017
      increased by INR 224.7 million (<money>US$ 3.5 million</money>), or 90%, to INR 474.9
      million (<money>US$ 7.4 million</money>) compared to the same period in 2016. The
      principal reason for the increase was capitalization of new projects
      during the period from December 31, 2016 to December 31, 2017.
    </p><p><i><b>Interest Expense, Net </b></i><br>Net interest expense during
      the quarter ended December 31, 2017 increased by INR 639.6 million (<money>US$
      10.0 million</money>), or 130%, to INR 1,130.0 million (<money>US$ 17.7 million</money>)
      compared to the same period in 2016. Interest expense increased on
      account of borrowings for new projects and was partially offset by the
      increased interest income on investments during the quarter ended
      December 31, 2017.
    </p><p><i><b>Gain / Loss on Foreign Currency Exchange </b></i><br>The Indian
      rupee depreciated against the <location value="LC/us" idsrc="xmltag.org">U.S.</location> dollar by INR 1.3 to <money>US$ 1.00</money> (2.0%)
      during the period from September 30, 2016 to December 31, 2016, while
      the Indian rupee appreciated against the <location value="LC/us" idsrc="xmltag.org">U.S.</location> dollar by INR 1.5 to <money>US$
      1.00</money> (2.2%) during the period from September 30, 2017 to December 31,
      2017. This appreciation during the period from September 30, 2017 to
      December 31, 2017 resulted in a foreign exchange gain of INR 90.8
      million (<money>US$ 1.4 million</money>), compared to a loss of INR 135.6 million
      during the same period in 2016.
    </p><p><i><b>Income Tax Expense / Benefit </b></i><br>The income tax benefit
      increased during the quarter ended December 31, 2017 by INR 485.6
      million (<money>US$ 7.6 million</money>) to INR 150.9 million (<money>US$ 2.4 million</money>),
      compared to the same period in 2016. The increase in the income tax
      benefit was primarily on account of the commissioning of new projects.
      During the current quarter, we recorded non-cash income tax benefit
      amounting to INR 150.9 million (<money>US$ 2.4 million</money>) and there was no cash
      outflow relating to income taxes during the period.
    </p><p><i><b>Net Loss </b></i><br>The net loss for the quarter ended December
      31, 2017 was INR 136.1 million (<money>US$ 2.1 million</money>), as compared to a net
      loss of INR 514.3 million for the quarter ended December 31, 2016, a
      decrease in loss of INR 378.1 million (<money>US$ 5.9 million</money>) as compared to
      the same period in 2016. This was primarily due to an increase in
      revenue during the quarter ended December 31, 2017, compared to December
      31, 2016.
    </p><p><i><b>Cash Flow and Working Capital </b></i><br>Cash generated from
      operating activities for the nine months ended December 31, 2017 of INR
      405.5 million (<money>US$ 6.4 million</money>), INR 181.8 million (<money>US$ 2.8 million</money>)
      higher than the prior comparable period, primarily due to an increase in
      revenue during the current period.
    </p><p>
      Cash used in investing activities, for the nine months ended December
      31, 2017 was INR 15,406.9 million (<money>US$ 241.3 million</money>), compared to INR
      14,601.8 million for the prior comparable period. The cash used in
      investing activities was higher due to purchases of property plant and
      equipment for new projects as compared to the prior comparable period.
    </p><p>
      Cash generated from financing activities was INR 16,757.0 (US$ 262.5
      million) for the nine months ended December 31, 2017, compared to INR
      17,182.9 million for the prior comparable period. During the nine months
      ended December 31, 2017, the Company raised INR 42,712.0 million (<money>US$
      669.2 million</money>) of non-convertible debentures and project debt, including
      green bonds.
    </p><p><i><b>Liquidity Position </b></i><br>As of December 31, 2017, the
      Company had INR 11,740.7 million (<money>US$ 183.9 million</money>) of cash, cash
      equivalents and current investments. The Company had undrawn project
      debt commitments of INR 3,631.6 million (<money>US$ 56.9 million</money>) as of
      December 31, 2017.
    </p><p><i><b>Adjusted EBITDA </b></i><br>Adjusted EBITDA was INR 1,226.9
      million (<money>US$ 19.2 million</money>) for the third quarter ended December 31,
      2017, compared to INR 696.4 million in the third quarter ended December
      31, 2016. The increase was primarily due to the increase in revenue
      during the period.
