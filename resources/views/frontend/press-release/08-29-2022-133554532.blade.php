                        <p><strong>August 29, 2022</strong></p>
                        <p><span class="legendSpanClass">
                                <location value="LU/in..newdel" idsrc="xmltag.org">NEW DELHI</location>
                            </span>, <span class="legendSpanClass">Aug. 29, 2022</span> /PRNewswire/ --&nbsp;Azure Power
                            Global Limited (NYSE: AZRE) ("Azure" or the "Company") today announced the appointment of
                            Mr. Rupesh Agarwal as its Acting Chief Executive Officer ("CEO"). Mr. Agarwal takes over
                            from Mr. Harsh Shah who has resigned as CEO with immediate effect. The Board will consider
                            appointment of a new permanent CEO in due course.</p>
                        <p>Mr. Agarwal, who joined the Company earlier this month as Chief Strategy &amp; Commercial
                            Officer after a period of acting as a Consultant to the Board, brings over two decades of
                            leadership experience in asset development and strategy consulting, advising leading
                            utilities and private equity investors on developing and managing their cleantech
                            investments. Mr. Agarwal has held leadership positions at Convergence Energy Services, a
                            Government of <location value="LC/in" idsrc="xmltag.org">India</location>-owned company,
                            Lightsource India, BDO, and EY. He was also the founder of AEM Energi &amp; Motum, a
                            zero-emission electric mobility company, which pioneered <location value="LC/in"
                                idsrc="xmltag.org">India's</location> first public electric bus trials. Mr. Agarwal will
                            work closely with the Company's Board to steer the Company at this critical juncture.</p>
                        <p>Alan Rosling, Chairman of the Board, said, "The Board very much regrets Harsh's unexpected
                            decision to leave the Company after joining only from July 1, 2022. We wish him well for the
                            future. We are pleased that Rupesh is stepping up to lead the Company at this time. The
                            Board will be working closely with Rupesh to ensure the Company's commercial objectives are
                            met as Azure scales its capacity in the growing Indian market."</p>
                        <p><strong>FY 2021-22 ANNUAL REPORT</strong></p>
                        <p>Following its August 12, 2022, announcement of a delay in the filing of the Company's annual
                            report on Form 20-F for the financial year ended March 31, 2022 ("FY 2021-22"), the Company
                            reiterates that it is working in close consultation with its advisers to close its annual
                            accounts. At this point, the Company is unable to give a firm timeline for submission of its
                            Form 20-F for FY 2021-22.</p>
                        <p><strong>PERFORMANCE UPDATE</strong></p>
                        <p>While the audit of the financial statements for FY 2021-22 has not yet been completed, key
                            operational highlights are set forth below. The information provided in this release is
                            unaudited and provisional and thus subject to completion of the Company's FY 2021-22
                            financial year-end audit. Investors are advised, therefore, that this information may be
                            subject to change.</p>
                        <p>During FY 2021-22, the Company fully commissioned its 600 MW AC project with Solar Energy
                            Corporation of <location value="LC/in" idsrc="xmltag.org">India</location> ("SECI")&nbsp;in
                            <location value="LS/in.rj" idsrc="xmltag.org">Rajasthan</location>, the largest owned and
                            operated single site solar project by any developer in the country. Subsequent to the end of
                            FY 2021-22, Power Purchase Agreements ("PPAs") have also been executed with SECI for the
                            Company's first 150 megawatts ("MWs") solar-wind hybrid project and the <span>PPA is in
                                process of execution* for the&nbsp;</span>Company's first 120 MWs wind project.</p>
                        <p><span>*C</span><em><span>orrected pursuant to our filing with US Securities and Exchange
                                    Commission on Form 6-K on August 31, 2022.&nbsp; PPA for 119MWs out of 120MWs was
                                    signed on August 31, 2022 and is expected to be amended subsequently to include the
                                    remaining 1MW.</span></em></p>
                        <p>Azure's Plant Load Factor ("PLF"), including the rooftop portfolio, for FY 2021-22, was
                            21.6%. Electricity generation, including the rooftop portfolio, during FY 2021-22 was 4,551
                            million kWh, an increase of 1,056 million kWh or 30%, over the fiscal year ended March 31,
                            2021.</p>
                        <p>For the projects under RG1 (3.575% Solar Green Bonds maturing in 2026) and RG2 (5.65% Solar
                            Green Bonds maturing in 2024), the PLF was 18.6% and <span>24.1%**</span>, respectively, and
                            electricity generation was 995 million kWh and 1,367 million kWh, respectively.</p>
                        <p><em><span>**Corrected pursuant to our filing with US Securities and Exchange Commission on
                                    Form 6-K on August 31, 2022</span></em></p>
                        <p><strong>WHISTLEBLOWER ALLEGATIONS</strong></p>
                        <p>The Company received a whistleblower complaint in May 2022 alleging potential procedural
                            irregularities and misconduct by certain employees at a plant belonging to one of its
                            subsidiaries. As part of the Company's review of these allegations, it discovered deviations
                            from safety and quality norms, and&nbsp;it has implemented mechanisms to remediate them and
                            in so doing strengthen safety and quality protocols. Azure's Audit Committee, with the
                            assistance of legal counsel and forensic accounting support, also identified evidence of
                            manipulation of project data and information by certain employees. The Company is
                            implementing immediate remedial measures, and Azure is initiating disclosure of the findings
                            to the appropriate authorities.</p>
                        <p><strong>PUBLIC INTEREST LITIGATIONS</strong></p>
                        <p>Regarding Azure's Power Purchase Agreements (PPAs) for its 2,333 MWs projects under the
                            manufacturing linked tender of 4,000 MWs, two Public Interest Litigations ("PILs") have been
                            filed in the High Court of <location value="LS/in.ap" idsrc="xmltag.org">Andhra Pradesh
                            </location>, challenging various aspects of the manufacturing tender and seeking to quash
                            the Andhra Pradesh Regulator's approval for procurement of capacity tied up with SECI.
                            Further, the tariff adoption for the capacities by the Central Electricity Regulatory
                            Commission has also been made subject to the outcome of the PILs. The Company is evaluating
                            the impact of these PILs</p>
                        <p><strong>FAVORABLE RESOLUTION OF LEGAL DISPUTES</strong></p>
                        <p>The Company has received a favorable Award from the Singapore International Arbitration
                            Centre, which has further been upheld by the Singapore High Court, in a proceeding initiated
                            by former Chairman and CEO Mr. Inderpreet Singh Wadhwa and erstwhile COO Mr. H.S. Wadhwa in
                            relation to the purchase price of their shares in Azure Power India Private Limited.</p>
                        <p>The Company has secured an order from a higher forum in <location value="LS/in.ap"
                                idsrc="xmltag.org">Andhra Pradesh</location> directing payments of pending and future
                            bills at the PPA tariff of its 50 MW project in <location value="LS/in.ap"
                                idsrc="xmltag.org">Andhra Pradesh</location>. Further to the rules (Late Payment
                            Surcharge Rules and related Matters, 2022) issued by Ministry of Power on June 3, 2022, the
                            offtaker Southern Power Distribution Company of Andhra Pradesh Ltd has initiated monthly
                            payments.</p>
                        <p>Azure recently received all dues from the offtaker Hubli Electricity Supply Company
                            ("HESCOM") for its 50 MW project in <location value="LS/in.ka" idsrc="xmltag.org">Karnataka
                            </location> pursuant to recent court orders.&nbsp;In respect of its 40 MW project in
                            <location value="LS/in.ka" idsrc="xmltag.org">Karnataka</location>, HESCOM has initiated
                            payment of the outstanding receivables.</p>
                        <p>The Company has received a favorable court order for its 10 MW project in <location
                                value="LS/in.br" idsrc="xmltag.org">Bihar</location> whereby the Company's claims on
                            account of a change in law for operation and maintenance charges have been allowed.</p>
                        <p>The Board believes that Azure's underlying business remains fundamentally strong. The Company
                            continues to operate in line with its budgets and business plans. Cash flow and collections
                            from customers continue in the ordinary course. The Company continues to service all its
                            debt payment obligations, and Azure's liquidity position remains strong, bolstered by its
                            recent <money>US$250 million</money> rights issue.</p>
