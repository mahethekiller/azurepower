                                            <p><span>EBENE, </span><span>
                                                    <location value="LC/mu" idsrc="xmltag.org">Mauritius</location>
                                                </span>, Feb. 10, 2021 /PRNewswire/ -- Azure Power Global Limited (NYSE:
                                                AZRE), a leading independent solar power producer in <location
                                                    value="LC/in" idsrc="xmltag.org">India</location>, today announced
                                                its consolidated results under United States Generally Accepted
                                                Accounting Principles ("GAAP") for the fiscal third quarter 2021, period
                                                ended December 31, 2020.</p>
                                            <p><b>Fiscal Third Quarter 2021 Period Ended December 31, 2020 Operating
                                                    Highlights:</b></p>
                                            <ul type="disc">
                                                <li>Operating Megawatts ("MW") were 1,987 MWs, as of December 31, 2020,
                                                    an increase of 10.0% over December 31, 2019. Operating and committed
                                                    megawatts were 7,115 MWs, as of December 31, 2020, an increase of
                                                    34.2% over December 31, 2019. Committed megawatts include 4,000 MWs
                                                    for which we have received Letters of Award ("LOA") but the Power
                                                    Purchase Agreements ("PPAs") have not yet been signed. </li>
                                                <li>Operating revenues for the quarter ended December 31, 2020 were INR
                                                    3,521 million (<money>US$ 48.2 million</money>), an increase of
                                                    15.6% over the quarter ended December 31, 2019. We estimate that our
                                                    revenues were negatively impacted by approximately INR 126 million (
                                                    <money>US$ 1.7 million</money>) on account of adverse weather
                                                    conditions resulting in low insolation, as compared to our initial
                                                    estimates, during the quarter. </li>
                                                <li>Net loss for the quarter ended December 31, 2020 was INR 1,088
                                                    million (<money>US$ 14.9 million</money>). During the quarter, our
                                                    results were negatively impacted by stock appreciation rights (SARs)
                                                    expense of INR 1,318 million (<money>US$ 18.1 million</money>).
                                                    Refer to the detailed explanation in the 'Stock Appreciation Rights
                                                    expense' section of the commentary below. </li>
                                                <li>Adjusted EBITDA for the quarter ended December 31, 2020 was INR
                                                    1,563 million (<money>US$ 21.3 million</money>), a decrease of 26.0%
                                                    over the quarter ended December 31, 2019. During the quarter, our
                                                    results were negatively impacted by an increase in stock
                                                    appreciation rights (SARs) expense of INR 1,318 million (<money>US$
                                                        18.1 million</money>). Refer the detailed explanation in the
                                                    'Stock Appreciation Rights expense' section of the commentary below.
                                                </li>
                                                <li>Non-GAAP Cash Flow to Equity ("CFe") from Operating Assets for the
                                                    quarter ended December 31, 2020 was INR 1,231 million (<money>US$
                                                        16.8 million</money>), an increase of 142.0% over the quarter
                                                    ended December 31, 2019.</li>
                                            </ul>
                                            <p><b><i>Key Operating Metrics</i></b></p>
                                            <p>Electricity generation during the quarter and nine-months ended December
                                                31, 2020 was 783.7 million kWh and 2,436.8 million kWh, respectively, an
                                                increase of 98.6 million kWh or 14.4%, over the quarter ended December
                                                31, 2019, and an increase of 441.8 million kWh, or 22.1%, over the nine
                                                months ended December 31, 2019. The increase in electricity generation
                                                was principally a result of an additional 183 MWs of AC (277 MWs DC)
                                                operating capacity commissioned since December 31, 2019. Our Plant Load
                                                Factor ("PLF") for the quarter and the nine months ended December 31,
                                                2020, was 19.3% and 20.1% respectively, compared to 17.7% and 18.6%,
                                                respectively, for the same comparable periods in 2019, which increased
                                                principally due to the addition of DC capacity and improved performance
                                                by our plants. </p>
                                            <p>We commissioned 153 MWs AC (236 MWs DC) during the three months ended
                                                December 31, 2020 and 179 MWs AC (264 MWs DC) during the nine months
                                                ended December 31, 2020.</p>
                                            <p>Project cost per megawatt operating (megawatt capacity per the PPA or AC)
                                                consists of costs incurred for one megawatt of new solar power plant
                                                capacity during the reporting period. The project cost per megawatt (DC)
                                                operating for the nine months ended December 31, 2020 decreased by INR
                                                3.2 million (<money>US$ 0.04 million</money>), or 9%, to INR 31.2
                                                million (<money>US$ 0.43 million</money>) primarily due to lower costs
                                                on account of the reduction in solar module prices for the projects
                                                commissioned during the period. The project cost per megawatt (AC)
                                                operating for the nine months ended December 31, 2020 was INR 45.0
                                                million (<money>US$ 0.62 million</money>), compared to INR 47.9 million,
                                                for the nine months ended December 31, 2019, on account of a reduction
                                                in solar module prices. Excluding the impact of safeguard duties, the DC
                                                and the AC costs per megawatt for the nine months ended December 31,
                                                2020 would have been lower by approximately INR 2.6 million (<money>US$
                                                    0.04 million</money>) and INR 2.7 million (<money>US$ 0.04 million
                                                </money>), respectively, and for the nine months in the prior year ended
                                                December 31, 2019, the DC and the AC costs per megawatt would have been
                                                lower by approximately INR 2.5 million and INR 4.1 million,
                                                respectively.</p>
                                            <p>As of December 31, 2020, our operating and committed megawatts were 7,115
                                                MWs, an increase of 1,815 MWs compared to December 31, 2019. Committed
                                                megawatts include 4,000 MWs for which we have received LOAs but the PPAs
                                                have not yet been signed. The Solar Energy Corporation of <location
                                                    value="LC/in" idsrc="xmltag.org">India</location> ("SECI") has
                                                informed us that so far there has not been adequate response from the
                                                state electricity distribution companies ("DISCOMs") for SECI to be able
                                                to sign the Power Sale Agreement ("PSA") at this stage even though we
                                                have a LOA. SECI has mentioned that they will be unable to sign PPAs
                                                until PSAs have been signed, and they have committed to inform Azure
                                                Power of developments in their efforts with the DISCOMS. Capital costs,
                                                interest rates and foreign exchange rates have improved since Azure
                                                Power won the 4 GW auction in December 2019 which have resulted in lower
                                                tariffs in other recent SECI auctions. We expect these savings likely
                                                will be passed on to state electricity distribution companies (DISCOMS).
                                                We expect a tariff markdown from the price discovered in the auction,
                                                which will facilitate signing of PSAs. We will continue our discussions
                                                with SECI towards signing PPAs in respect of the 4GW tender and expect
                                                the PPAs to be signed in tranches over a period of time.</p>
