                        <p><strong> June 12, 2020 </strong></p>
                        <p>EBENE, <location value="LC/mu" idsrc="xmltag.org">Mauritius</location>, June 12, 2020
                            /PRNewswire/ --<b>&nbsp;</b>Azure Power Global Limited (NYSE: AZRE), as leading independent
                            solar power producer in <location value="LC/in" idsrc="xmltag.org">India</location>, today
                            announced its consolidated results under United States Generally Accepted Accounting
                            Principles ("GAAP") for the fiscal fourth quarter ended March 31, 2020.</p>
                        <p><b>Fiscal Year and Fourth Quarter 2020 Period Ended March 31, 2020 Operating Highlights:</b>
                        </p>
                        <ul type="disc">
                            <li>Operating Megawatts ("MW") were 1,808 MWs, as of March 31, 2020, an increase of 25% over
                                March 31, 2019. Operating and Committed Megawatts were 7,115 MWs, as of year ended March
                                31, 2020, an increase of 112% over the year ended March 31, 2019. 2,000 MWs of Committed
                                Capacity is a greenshoe option that has been exercised by the Company as part of an
                                auction that was won but this capacity has yet to receive a Letter of Award. </li>
                            <li>Revenue for the quarter ended March 31, 2020 was INR 3,675 million (<money>US$ 48.7
                                    million</money>), an increase of 29% over the quarter ended March 31, 2019. </li>
                            <li>Net loss for the quarter ended March 31, 2020 was INR 394 million (<money>US$ 5.2
                                    million</money>). During the quarter, our results were negatively impacted by higher
                                charges amounting to INR 551 million (<money>US$ 7.3 million</money>), partially offset
                                by higher revenue, refer the detailed explanation in the net loss section of the
                                commentary below.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </li>
                            <li>Non-GAAP Adjusted EBITDA for the quarter ended March 31, 2020 was INR 2,647 million (
                                <money>US$ 35.1 million</money>), an increase of 24% over the quarter ended March 31,
                                2019, despite being negatively impacted by additional expenses of INR 169 million (
                                <money>US$ 2.2 million</money>) related to management transition and accruals related to
                                stock appreciation rights included in general and administrative expenses. </li>
                            <li>Non-GAAP Free Cash to Equity for Operating Assets for fiscal year 2020 was INR 3,237
                                million (<money>US$ 43.0 million</money>), an increase of INR 424 million or 15% over
                                fiscal year 2019. </li>
                        </ul>
                        <p><b><i>Key Operating Metrics</i></b></p>
                        <p>Electricity generation during the quarter and year ended March 31, 2020 was 868 million kWh
                            and 2,870&nbsp;million kWh, respectively, an increase of 343 million kWh or 65%, over the
                            quarter ended March 31, 2019, and an increase of 1,136&nbsp;million kWh, or 66%, over the
                            year ended March 31, 2019.&nbsp;The increase in electricity generation was principally a
                            result of additional operating capacity during the period driven by the commissioning of new
                            projects. Our&nbsp;Plant Load Factor ("PLF")&nbsp;for the quarter and the year ended March
                            31, 2020, was 22.3% and 19.5%, respectively, compared to 20.5 % and 18.6%, respectively, for
                            the same comparable period in 2019. </p>
                        <p>During the fiscal year, we completed 367 MWs (AC) and 610 MWs (DC). During the fourth
                            quarter, 4 MWs (AC) and 11 MWs (DC) were put into operation. Project cost per megawatt
                            operating consists of costs incurred for one megawatt of new solar power plant capacity
                            during the reporting period. The project cost per megawatt (DC) operating for the year ended
                            March 31, 2020 decreased by INR 5.2 million (<money>US$ 0.07 million</money>), or 13%, to
                            INR 35.5 million (<money>US$ 0.47 million</money>) primarily due to lower costs on account
                            of the reduction in solar module prices for the projects commissioned during the period. The
                            project cost per megawatt (AC&nbsp;or megawatt capacity per the PPA)&nbsp;operating for the
                            year ended March 31, 2020 was INR 48.9 million (<money>US$ 0.65 million</money>), compared
                            to INR 50.4 million, for the year ended March 31, 2019, on account of the reduction in solar
                            module prices which was partially offset by additional safeguard duties paid by the Company.
                            Excluding the impact of safeguard duties, the DC and the AC costs per megawatt for fiscal
                            year 2020, would have been lower by approximately INR 2.9 million (<money>US$ 0.04 million
                            </money>) and INR 4.9 million (<money>US$ 0.07 million</money>), respectively.</p>
                        <p>As of March 31, 2020, our operating and committed megawatts increased by 3,759 MWs to 7,115
                            MWs compared to March 31, 2019. 2,000 MWs of Committed Capacity is a greenshoe option that
                            has been exercised by the Company as part of an auction that was won but this capacity has
                            yet to receive a Letter of Award. </p>


