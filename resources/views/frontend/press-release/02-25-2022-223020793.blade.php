                        </div>
                        <p><b>Operating Highlights:</b></p>
                        <ul type="disc">
                            <li>Megawatts ("MWs") Operating* were 2,523 MWs, as of December 31, 2021, an increase of 37%
                                over December 31, 2020. Operating, Contracted &amp; Awarded MWs* were 7,425 MWs, as of
                                December 31, 2021. <br><br>Contracted &amp; Awarded megawatts included 1,537 MWs for
                                which we had received Letters of Award ("LOA"), but the Power Purchase Agreements
                                ("PPAs") had not been signed as of December 31, 2021. </li>
                            <li>Operating revenues for the quarter ended December 31, 2021, were INR 4,480 million (
                                <money>US$ 60.2 million</money>), an increase of 27% over the quarter ended December 31,
                                2020. </li>
                            <li>Net loss for the quarter ended December 31, 2021, was INR 610 million (<money>US$ 8.2
                                    million</money>) against the net loss of INR 1,088 million for the quarter ended
                                December 31, 2020. The decrease in loss in current quarter was primarily due to
                                additional revenue from customers, reversal of stock appreciation rights (SARs) expense,
                                offset by an increase in interest and tax expense. <i>(Refer the detailed explanation in
                                    the Net loss/(profit) section below)</i></li>
                            <li>Adjusted EBITDA for the quarter ended December 31, 2021, was INR 3,935 million (<money>
                                    US$ 52.9 million</money>), an increase of 152% over the quarter ended December 31,
                                2020. The increase was primarily due to additional revenue from new projects, revenue
                                from customers and reversal of stock appreciation rights (SARs) expense during the
                                quarter ended December 31, 2021. </li>
                            <li>Non-GAAP Cash Flow to Equity ("CFe") from operating assets for the quarter ended
                                December 31, 2021, was INR 1,941 million (<money>US$ 26.1 million</money>), an increase
                                of 58% over the quarter ended December 31, 2020. The increase was primarily due to
                                projects which were commissioned after the quarter ended December 31, 2020, and
                                incremental revenue from customers.</li>
                        </ul>
                        <p>*Megawatts Operating and Megawatts Operating, Contracted &amp; Awarded exclude the Rooftop
                            portfolio, for which we entered into an agreement to sell in April 2021. We excluded 153 MWs
                            from our Operating, Contracted &amp; Awarded portfolio as of December 31, 2021, and the
                            prior comparative period. We excluded 153 MWs from our Operating portfolio as of December
                            31, 2021, and 147 MWs from prior comparable period. <i>The exclusion from prior period is
                                for the purpose of comparison with current period, however we continue to consolidate
                                these assets as of December 31, 2021.</i></p>
                        <p><b><i>Key Operating Metrics</i></b></p>
                        <p>Electricity generation during the quarter and nine-months ended December 31, 2021, was 1,068
                            million kWh and 3,181 million kWh, respectively, an increase of 284 million kWh or 36%, over
                            the quarter ended December 31, 2020, and an increase of 744 million kWh or 31%, over the
                            nine months ended December 31, 2020. The increase in electricity generation was principally
                            a result of an additional 689 MWs of AC (805 MWs DC) operating capacity, including our
                            Rooftop portfolio commissioned since December 31, 2020. </p>
                        <p>Our Plant Load Factor ("PLF") for the quarter and nine months ended December 31, 2021, was
                            19.6% and 21.1%, compared to 19.3% and 20.1%, respectively, for the same comparable periods
                            in 2020, which increased principally due to the addition of AC and DC capacity and improved
                            performance by our plants, including Rooftop portfolio. Our Plant Load Factor
                            ("PLF"),&nbsp;excluding rooftop portfolio, for the quarter and nine months ended December
                            31, 2021, was 20.5% and 22.0% respectively.</p>
                        <p>We commissioned 313 MWs AC&nbsp;during the three months ended December 31, 2021, and
                            533&nbsp;MWs AC during the nine months ended December 31, 2021, against 153 MWs AC (236 MWs
                            DC) during the comparative three months and 179 MWs AC (264 MWs DC) during the nine months
                            ended December 31, 2020.&nbsp;</p>
                        <p>Project cost per megawatt operating (megawatt capacity per the PPA or AC) consists of costs
                            incurred for one megawatt of newly commissioned solar power plant capacity during the
                            reporting period. The project cost per megawatt (DC) operating for the nine months ended
                            December 31, 2021, was INR 33.9 million (<money>US$ 0.46 million</money>), compared to INR
                            31.2 million for the nine months ended December 31, 2020. The project cost per megawatt (AC)
                            operating for the nine months ended December 31, 2021, was INR 35.9 million (<money>US$ 0.48
                                million</money>), compared to INR 45.0 million, for the nine months ended December 31,
                            2020. Excluding the impact of safeguard duties, the DC and the AC costs per megawatt for the
                            nine months ended December 31, 2021, would have been lower by approximately INR 2.0 million
                            (<money>US$ 0.03 million</money>) and INR 2.1 million (<money>US$ 0.03 million</money>),
                            respectively, and for the nine months ended December 31, 2020, the DC and the AC costs per
                            megawatt would have been lower by approximately INR 2.6 million and INR 2.7 million,
                            respectively</p>
                        <p><b><i>Megawatts Operating and Megawatts Contracted &amp; Awarded</i></b></p>
                        <p>We measure the rated capacity of our plants in megawatts. Rated capacity is the expected
                            maximum output that a solar power plant can produce without exceeding its design limits. We
                            believe that tracking the growth in aggregate megawatt rated capacity is a measure of the
                            growth rate of our business.</p>
                        <p>"Megawatts Operating" represents the aggregate cumulative megawatts rated capacity of solar
                            power plants that are commissioned and operational as of the reporting date.</p>
                        <p><i>"</i>Megawatts Contracted &amp; Awarded<i>"</i> represents the aggregate megawatt rated
                            capacity of solar power plants pursuant to customer PPAs signed, allotted or won in an
                            auction but not commissioned and operational as of the reporting date. </p>
                        <p>As of December 31, 2021, our Operating, Contracted &amp; Awarded megawatts were 7,425 MWs.
                            Contracted &amp; Awarded megawatts included 1,537 MWs for which we had received LOAs but the
                            PPAs had not been signed as of December 31, 2021. There was an increase of 470 MWs compared
                            to prior comparable period. The Company has received letter of awards (LOA), for its first
                            120 MWs wind project and 150 MWs solar – wind hybrid project from Solar Energy Corporation
                            of <location value="LC/in" idsrc="xmltag.org">India</location> (SECI) and 200 MWs solar –
                            wind hybrid project from Maharashtra State Electricity Distribution Company Limited
                            (MSEDCL). During the quarter, we signed PPAs for 600 MWs and 2,333 MWs with SECI at a fixed
                            tariff of INR 2.54 per kWh and INR 2.42 per kWh, respectively, for supply power for 25
                            years, as a part of 4,000 MWs manufacturing linked projects.</p>
                        <p><b>About Azure Power</b></p>
                        <p>Azure Power is a leading independent sustainable energy solutions provider, and power
                            producer in <location value="LC/in" idsrc="xmltag.org">India</location> on a mission to
                            create value for all stakeholders through high-performance Renewable Energy assets to
                            displace 230 million tons of global emissions by 2030.</p>
                        <p>We developed <location value="LC/in" idsrc="xmltag.org">India's</location> first utility
                            scale solar project in 2009 and since then, Azure Power has grown rapidly to become a leader
                            in developing and operating renewable energy projects in the country. We have substantially
                            reduced our total project cost, which includes a significant reduction in balance of systems
                            costs due in part to our value engineering, design, and procurement efforts.</p>
