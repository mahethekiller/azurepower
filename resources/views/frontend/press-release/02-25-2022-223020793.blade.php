                        <p>EBENE, <location value="LC/mu" idsrc="xmltag.org">Mauritius</location>, Feb. 25, 2022
                            /PRNewswire/ -- Azure Power Global Limited (NYSE: AZRE), a leading independent renewable
                            power producer in <location value="LC/in" idsrc="xmltag.org">India</location>, today
                            announced its consolidated results under United States Generally Accepted Accounting
                            Principles ("GAAP") for the third quarter of fiscal 2022, ended December 31, 2021.</p>
                        <div class="PRN_ImbeddedAssetReference" id="DivAssetPlaceHolder5073">
                            <p><a href="https://mma.prnewswire.com/media/1754766/Azure_Power_Q3_Fiscal_2022.pdf?p=original"
                                    target="_blank" rel="nofollow"><img
                                        src="https://mma.prnewswire.com/media/1754766/Azure_Power_Q3_Fiscal_2022.pdf?p=pdfthumbnail"
