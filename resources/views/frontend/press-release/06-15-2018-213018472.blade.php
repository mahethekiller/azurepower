                        <p><strong> June 15, 2018 </strong></p>
                        <p> EBENE, <location value="LC/mu" idsrc="xmltag.org">Mauritius</location>--(BUSINESS WIRE)--
                            Azure Power Global Limited (NYSE: AZRE), a leading independent solar
                            power producer in <location value="LC/in" idsrc="xmltag.org">India</location>, today
                            announced its consolidated results under
                            United States Generally Accepted Accounting Principles (“GAAP”) for the
                            fourth quarter ended March&nbsp;31, 2018.
                        </p>
                        <p><b>Fourth Quarter 2018 Period Ended March 31, 2018 Operating Highlights:</b></p>
                        <ul>
                            <li class="bwlistitemmargb">
                                Operating&nbsp;Megawatts were 911 MW, as of March 31, 2018, an increase of
                                40% over March 31, 2017.
                            </li>
                            <li class="bwlistitemmargb">
                                Operating&nbsp;&amp; Committed Megawatts were 1,871 MW, as of March&nbsp;31, 2018,
                                an increase of 75% over March 31, 2017.
                            </li>
                            <li class="bwlistitemmargb">
                                Revenue for the quarter was INR 2,259.0&nbsp;million (<money>US$34.7 million</money>),
                                an
                                increase of 71% over the quarter ended March 31, 2017.
                            </li>
                            <li class="bwlistitemmargb">
                                Adjusted EBITDA for the quarter was INR 1,625.5&nbsp;million (<money>US$25.0
                                    million</money>), an increase of 67% over the quarter ended March 31, 2017.
                            </li>
                        </ul>
                        <p><i><b>Key Operating Metrics</b></i></p>
                        <p>
                            Electricity generation during the fiscal year ended March 31, 2018
                            increased by 618&nbsp;million kWh, or 100%, to 1,236&nbsp;million kWh, compared to
                            the same period in 2017. The increase in electricity generation was
                            principally a result of additional capacity operating during the period.
                        </p>
                        <p>
                            Total revenue during the fiscal year ended March 31, 2018 was INR
                            7,700.6&nbsp;million (<money>US$ 118.3 million</money>), up 84% from INR
                            4,183.0&nbsp;million
                            during the same period in 2017. The increase in revenue was primarily
                            driven by the commissioning of new projects.
                        </p>
                        <p>
                            Project cost per megawatt operating (megawatt capacity per the power
                            purchase agreement) consists of costs incurred for one megawatt of new
                            solar power plant capacity during the reporting period. The project cost
                            per megawatt operating for the fiscal year ended March 31, 2018
                            increased by INR 1.7&nbsp;million (<money>US$ 0.03 million</money>) to INR 51.0&nbsp;million
                            (<money>US$
                                0.78 million</money>), as compared to the same period in 2017. The project cost
                            per megawatt was marginally higher due to the use of higher-cost
                            domestic modules as required by Power Purchase Agreements “PPA” and use
                            of purchased land compared to lower-cost open source modules and leased
                            land in the corresponding previous period.
                        </p>
                        <p>
                            As of March 31, 2018, our operating and committed megawatts increased by
                            802 MW to 1,871 MW compared to March 31, 2017 as a result of winning new
                            projects.
                        </p>
