                        <p><b>Fourth Quarter 2018 Period ended March 31, 2018 Consolidated
                                Financial Results:</b></p>
                        <p><i><b>Operating Revenue</b></i></p>
                        <p>
                            Operating revenue in the quarter ended March 31, 2018 was INR
                            2,259.0&nbsp;million (<money>US$ 34.7 million</money>), an increase of 71% from INR
                            1,317.6&nbsp;million over the same period in 2017. The increase in revenue
                            was driven by the commissioning of new projects.
                        </p>
                        <p><i><b>Cost of Operations</b></i></p>
                        <p>
                            Cost of operations in the quarter ended March 31, 2018 increased by 65%
                            to INR 215.4&nbsp;million (<money>US$ 3.3 million</money>) from INR 130.7&nbsp;million in
                            the
                            same period in 2017. The increase was primarily due to plant maintenance
                            cost for newly commissioned projects which was partially offset by the
                            implementation of improved O&amp;M methods which improved plant productivity.
                        </p>
                        <p><i><b>General and Administrative Expenses</b></i></p>
                        <p>
                            General and administrative expenses for the quarter ended March 31, 2018
                            increased by INR 205.7&nbsp;million (<money>US$ 3.2 million</money>), to INR
                            418.2&nbsp;million
                            (<money>US$ 6.4 million</money>) compared to the same period in 2017. The increase was
                            primarily due to increase in personnel costs to support the Company’s
                            growth and higher professional charges compared to previous comparable
                            quarter.
                        </p>
                        <p><i><b>Depreciation and Amortization Expenses</b></i></p>
                        <p>
                            Depreciation and amortization expenses during the quarter ended March
                            31, 2018 increased by INR 210.8&nbsp;million (<money>US$ 3.2 million</money>), or 67%, to
                            INR 524.8&nbsp;million (<money>US$ 8.1 million</money>) compared to the same period in 2017.
                            The principal reason for the increase was capitalization of new projects
                            during the period from December 31, 2016 to March 31, 2018.
                        </p>
                        <p><i><b>Interest Expense, Net</b></i></p>
                        <p>
                            Net interest expense during the quarter ended March 31, 2018 increased
                            by INR 202.6&nbsp;million (<money>US$ 3.1 million</money>), or 32%, to INR
                            833.7&nbsp;million
                            (<money>US$ 12.8 million</money>) compared to the same period in 2017. Interest expense
                            increased on account of borrowings for new projects and was partially
                            offset by the increased interest income on investments during the
                            quarter ended March 31, 2018. This includes one-time reclassification
                            from interest expense to Income tax expense of INR 136.2 million (<money>US$
                                2.1 million</money>), due to issuance of Green bonds.
                        </p>
                        <p><i><b>Gain / Loss on Foreign Currency Exchange</b></i></p>
                        <p>
                            The Indian rupee appreciated against the <location value="LC/us" idsrc="xmltag.org">U.S.
                            </location> dollar by INR 3.1 to <money>US$
                                1.00</money> (4.5%) during the period from December&nbsp;31, 2016 to March 31, 2017,
                            while the Indian rupee depreciated against the <location value="LC/us"
                                idsrc="xmltag.org">U.S.</location> dollar by INR 1.3 to
                            <money>US$ 1.00</money> (2.0%) during the period from December 31, 2017 to March 31,
                            2018. This depreciation during the period from December 31, 2017 to
                            March 31, 2018 resulted in a foreign exchange loss of INR 98.3&nbsp;million
                            (<money>US$ 1.5 million</money>), compared to a gain of INR 309.2&nbsp;million during the
                            same period in 2017.
                        </p>
                        <p><i><b>Income Tax Expense / Benefit</b></i></p>
                        <p>
                            The income tax expense decreased during the quarter ended March 31, 2018
                            by INR 624.0&nbsp;million (<money>US$ 9.6 million</money>) to INR 21.1&nbsp;million (<money>
                                US$ 0.3
                                million</money>), compared to income tax expense of INR 645.2 million in the
                            same period in 2017. During the current quarter, we recorded a non-cash
                            deferred tax income amounting to INR 115.1&nbsp;million (<money>US$ 1.8 million</money>),
                            was primarily on account of new projects commissioned during the quarter
                            and there was cash outflow of INR 136.2 million (<money>US$2.1 million</money>) related
                            to current income taxes.
                        </p>
                        <p><i><b>Net income/ loss</b></i></p>
                        <p>
                            The net income for the quarter ended March 31, 2018 was INR
                            147.6&nbsp;million (<money>US$ 2.3 million</money>), as compared to a net loss of INR
                            306.7&nbsp;million for the quarter ended March 31, 2017, an increase in net
                            income by INR 454.3&nbsp;million (<money>US$ 7.0 million</money>) as compared to the same
                            period in 2017. This was primarily due to an increase in revenue and
                            economics of scale during the quarter ended March 31, 2018, compared to
                            March 31, 2017.
                        </p>
                        <p><i><b>Cash Flow and Working Capital</b></i></p>
                        <p>
                            Cash generated from operating activities for the fiscal year ended March
                            31, 2018 of INR 1,839.1&nbsp;million (<money>US$ 28.2 million</money>), INR
                            1,866.3&nbsp;million
                            (<money>US$ 28.7 million</money>) higher than the prior comparable period, primarily
                            due to an increase in revenue during the current period.
                        </p>
                        <p>
                            Cash used in investing activities, for the fiscal year ended March 31,
                            2018 was INR 15,772.2&nbsp;million (<money>US$ 242.2 million</money>), compared to INR
                            21,944.3&nbsp;million for the prior comparable period. The cash used in
                            investing activities was lower due to realisation of cash from
                            maturities of available for sale investment and restricted term deposits
                            and used in purchase of property, plant and equipment. The purchases of
                            property plant and equipment for new projects as compared to the prior
                            comparable period, was higher by INR 4,207.9 million (<money>US$ 64.6 million</money>).
                        </p>
                        <p>
                            Cash generated from financing activities was INR 16,816.1 (US$ 258.3
                            million) for the fiscal year ended March 31, 2018, compared to INR
                            24,331.5&nbsp;million for the prior comparable period. During the fiscal year
                            ended March 31, 2018, the Company raised INR 43,807.3&nbsp;million (<money>US$ 672.8
                                million</money>) of non-convertible debentures and project debt, including green
                            bonds.
                        </p>
                        <p><i><b>Liquidity Position</b></i></p>
                        <p>
                            As of March 31, 2018, the Company had INR 9,730.1&nbsp;million (<money>US$ 149.4
                                million</money>) of cash, cash equivalents and current investments. The Company
                            had undrawn project debt commitments of INR 3,398.6&nbsp;million (<money>US$ 52.2
                                million</money>) as of March 31, 2018.
                        </p>
                        <p><i><b>Adjusted EBITDA</b></i></p>
                        <p>
                            Adjusted EBITDA was INR 1,625.5&nbsp;million (<money>US$ 25.0 million</money>) for the
                            fiscal fourth quarter period ended 2018, compared to INR 974.4&nbsp;million
                            in the fourth quarter ended March 31, 2017. The increase was primarily
                            due to the increase in revenue and economics of scale during the period.
                        </p>
