                                            <p><b>Fiscal Third Quarter 2019 Period ended December 31, 2018 Consolidated
                                                    Financial Results:</b></p>
                                            <p><b><i>Operating Revenues</i></b></p>
                                            <p>Operating revenues during the three months ended December 31, 2018
                                                increased by INR 690.9 million (<money>US$ 9.9 million</money>), or 40%,
                                                to INR 2,430.8 million (<money>US$ 34.9 million</money>) compared to the
                                                same period in 2017. The increase in revenue for the three months ended
                                                December 31, 2018 is on account of projects commissioned by the Company
                                                since last year.</p>
                                            <p><b><i>Cost of Operations (Exclusive of Depreciation and
                                                        Amortization)</i></b></p>
                                            <p>Cost of operations during the three months ended December 31, 2018
                                                increased by INR 60.6 million (<money>US$ 0.9 million</money>), or 38%,
                                                to INR</p>
                                            <p>219.0 million (<money>US$ 3.1 million</money>) compared to the same
                                                period in 2017. The increase was primarily due to increase in plant
                                                maintenance costs related to newly operational projects. The operating
                                                cost per megawatt during the three-month period ended December 31, 2018
                                                was INR 0.19 million, a decrease of INR 0.01 million per megawatt as
                                                compared to the same period in 2017.</p>
                                            <p class="prntal"><b><i>General and Administrative Expenses</i></b></p>
                                            <p>General and administrative expenses during the three months ended
                                                December 31, 2018 increased by INR 19.7 million (<money>US$ 0.3 million
                                                </money>), or 6%, to INR 374.3 million (<money>US$ 5.4 million</money>)
                                                compared to the same period in 2017. The increase in general and
                                                administrative expenses was lower than the increase in revenue due to
                                                economies of scale of operations.</p>
                                            <p><b><i>Depreciation and Amortization</i></b></p>
                                            <p>Effective October 1, 2018, the Company extended the estimated useful life
                                                of most of its utility scale projects from 25 years to 35 years. This
                                                change in accounting estimate was based on the Company's various
                                                technical evaluations and tests, through which the Company estimated
                                                that its solar modules will continue to generate power for at least 35
                                                years at high efficiency levels.</p>
                                            <p>Depreciation and amortization expenses during the three months ended
                                                December 31, 2018 increased by INR 1.0 million, or 0.2%, to INR 476.0
                                                million (<money>US$ 6.8 million</money>) compared to the same period in
                                                2017. There was no significant change in the depreciation and
                                                amortization expense as the additional depreciation on new projects
                                                commissioned since last year was offset by a decrease in depreciation
                                                expense on account of change in useful life.</p>
                                            <p><b><i>Interest Expense, Net</i></b></p>
                                            <p>Net interest expense during the three months ended December 31, 2018
                                                decreased by INR 14.1 million (<money>US$ 0.2 million</money>), or 1%,
                                                to INR 1,115.8 million (<money>US$ 16.0 million</money>) compared to the
                                                same period in 2017. Interest expense decreased on account of increase
                                                in interest income partly offset by borrowings for new projects during
                                                the quarter ended December 31, 2018.</p>
                                            <p><b><i>Loss on Foreign Currency Exchange</i></b></p>
                                            <p>Foreign exchange loss during the three months ended December 31, 2018
                                                increased by INR 108.7 million (<money>US$ 1.6 million</money>) compared
                                                to the same period in 2017 to a loss of INR 17.9 million (<money>US$ 0.3
                                                    million</money>). The foreign exchange loss increased primarily on
                                                account of realized foreign currency payments.</p>
                                            <p><b><i>Income Tax Expense / (Benefit)</i></b></p>
                                            <p>Income tax expense increased by INR 213.5 million (<money>US$ 3.1 million
                                                </money>) to INR 62.5 million (<money>US$ 0.9 million</money>) during
                                                the three months ended December 31, 2018 reflecting an increase in
                                                profits during the third quarter of 2019.</p>
                                            <p><b><i>Net Profit</i></b></p>
                                            <p>The net profit for the quarter ended December 31, 2018 was INR 165.3
                                                million (<money>US$ 2.4 million</money>) as compared to a net loss of
                                                INR 136.2 million for the quarter ended December 31, 2017, reflecting an
                                                improvement of INR 301.5 million (<money>US$ 4.3 million</money>) as
                                                compared to the same period in 2017. The increase was primarily due to
                                                an increase in revenue and economies of scale on operating costs
                                                achieved during the period.</p>
                                            <p><b><i>Cash Flow and Working Capital</i></b></p>
                                            <p>Cash generated from operating activities for the nine months ended
                                                December 31, 2018 was INR 789.0 million (<money>US$ 11.3 million</money>
                                                ), an increase of INR 383.5 million (<money>US$ 5.5 million</money>) as
                                                compared to the prior comparable period, primarily due to increase in
                                                revenue during the period.</p>
                                            <p>Cash used in investing activities for the nine months ended December 31,
                                                2018 was INR 13,114.3 million (<money>US$ 188.5 million</money>)
                                                compared to INR 16,303.3 million for the prior comparable period. The
                                                cash outflow was primarily due to INR 14,590.9 million (<money>US$ 209.7
                                                    million</money>) incurred to purchase Property, Plant and Equipment.
                                            </p>
                                            <p class="prntal">Cash generated from financing activities increased by INR
                                                8,357.4 million (<money>US$ 120.1 million</money>) to INR 25,114.4
                                                million (<money>US$ 360.9 million</money>), primarily on account of the
                                                public issuance of equity shares and new debt raised during the period.
                                            </p>
                                            <p><b><i>Liquidity Position</i></b></p>
                                            <p>As of December 31, 2018, the Company had INR 17,451.0 million (<money>US$
                                                    250.8 million</money>) of cash, cash equivalents and current
                                                investments. The Company had undrawn project debt commitments of INR
                                                12,404.3 million (<money>US$ 178.3 million</money>) as of December 31,
                                                2018 and a working capital facility of INR 7,238.0 million (<money>US$
                                                    104.0 million</money>).</p>
                                            <p><b><i>Adjusted EBITDA</i></b></p>
                                            <p>Adjusted EBITDA during the three months ended December 31, 2018 increased
                                                by INR 610.6 million (<money>US$ 8.8 million</money>) or 50%, as
                                                compared to the same period in 2017 to INR 1,837.5 million (<money>US$
                                                    26.4 million</money>). The increase was primarily due to the
                                                increase in revenue and economies of scale on operating costs achieved
                                                during the period.</p>
                                            <p><b><i>Earnings per share</i></b></p>
                                            <p>The earnings per share for the three months ended December 31, 2018 was
                                                <money>US$ 0.05</money>, as compared to a loss per share of <money>US$
                                                    0.03</money> for the prior comparable period.</p>
