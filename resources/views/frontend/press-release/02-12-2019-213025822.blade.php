                                            <p>EBENE, <location value="LC/mu" idsrc="xmltag.org">Mauritius</location>,
                                                Feb.&nbsp;12, 2019 /PRNewswire/ -- Azure Power Global Limited (NYSE:
                                                AZRE), a leading independent solar power producer in <location
                                                    value="LC/in" idsrc="xmltag.org">India</location>,
                                                today&nbsp;announced its consolidated results under United States
                                                Generally Accepted Accounting Principles ("GAAP") for the fiscal third
                                                quarter ended December 31, 2018.</p>
                                            <p><b>Fiscal Third Quarter 2019 Period Ended December 31, 2018 Operating
                                                    Highlights:</b></p>
                                            <ul type="disc">
                                                <li>Operating Megawatts (MW) were 1,169 MW as of December 31, 2018, an
                                                    increase of 45% over December 31, 2017. </li>
                                                <li>Operating &amp; Committed Megawatts were 3,059 MW as of December 31,
                                                    2018, an increase of 94% over December 31, 2017. </li>
                                                <li>Revenue for the quarter was INR 2,430.8 million (<money>US$ 34.9
                                                        million</money>), an increase of 40% over the quarter ended
                                                    December 31, 2017. </li>
                                                <li>Adjusted EBITDA for the quarter was INR 1,837.5 million (<money>US$
                                                        26.4 million</money>), an increase of 50% over the quarter ended
                                                    December 31, 2017.</li>
                                            </ul>
                                            <p><b><i>Key Operating Metrics</i></b></p>
                                            <p>Electricity generation during the nine months ended December 31, 2018
                                                increased by 353 million kWh, or 41%, to 1,208 million kWh compared to
                                                the same period in 2017. The increase in electricity generation was
                                                principally a result of additional capacity operating during the period.
                                            </p>
                                            <p>Total revenue during the nine months ended December 31, 2018 was INR
                                                7,079.0 million (<money>US$ 101.7 million</money>), up by 30% from INR
                                                5,441.6 million during the same period in 2017. The increase in revenue
                                                was primarily driven by the commissioning of new projects.</p>
                                            <p>Project cost per megawatt operating (megawatt capacity in DC) consists of
                                                costs incurred for one megawatt of new solar power plant during the
                                                reporting period. The project cost per megawatt operating for the nine
                                                months ended December 31, 2018 decreased by INR 3.2 million (<money>US$
                                                    0.05 million</money>) to INR 44.2 million (<money>US$ 0.64 million
                                                </money>). The project cost per megawatt was lower for the nine months
                                                ended December 31, 2018 than the comparable period in the prior year due
                                                to lower costs on account of a decline in solar module prices and
                                                efficiency gains in balance of system costs.</p>
                                            <p>As of December 31, 2018, our operating and committed megawatts increased
                                                by 1,479 MW compared to December 31, 2017 to 3,059 MW as a result of
                                                winning new projects.</p>
