                                requirements necessary to service interest or principal payments on
                                our outstanding debt;
                            </li>
                            <li class="bwlistitemmargb">
                                it does not reflect payments made or future requirements for income
                                taxes; and
                            </li>
                            <li class="bwlistitemmargb">
