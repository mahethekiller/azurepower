                        <p> <b>November 15, 2019</b> </p>
                        <p></p>
                        <p>EBENE, <location value="LC/mu" idsrc="xmltag.org">Mauritius</location>, Nov. 15, 2019
                            /PRNewswire/ --<b>&nbsp;</b>Azure Power Global Limited (NYSE: AZRE), a leading independent
                            solar power producer in <location value="LC/in" idsrc="xmltag.org">India</location>, today
                            announced its consolidated results under United States Generally Accepted Accounting
                            Principles ("GAAP") for the fiscal second quarter 2020, period ended September&nbsp;30,
                            2019.</p>
                        <p><b>Fiscal Second Quarter 2020 Period Ended September&nbsp;30, 2019 Operating Highlights:</b>
                        </p>
                        <ul type="disc">
                            <li>Operating Megawatts ("MW") were 1,798 MW, as of September 30, 2019, an increase of 77%
                                over September 30, 2018. </li>
                            <li>Operating and Committed Megawatts were 3,370 MWs, as of quarter ended September 30,
                                2019, an increase of 10% over the quarter ended September 30, 2018.&nbsp;&nbsp; </li>
                            <li>Revenue for the quarter ended September 30, 2019 was INR 2,846.6 million (<money>US$
                                    40.3 million</money>), an increase of 28% over the quarter ended September 30, 2018.
                            </li>
                            <li>Adjusted EBITDA for the quarter ended September 30, 2019 was INR 2,078.5 million (
                                <money>US$ 29.4 million</money>), an increase of 15% over the quarter ended September
                                30, 2018 <br></li>
                        </ul>
                        <p><b><i>Key Operating Metrics</i></b></p>
                        <p>Electricity generation during the quarter and six-months ended September 30, 2019 was 610
                            million kWh and 1,310 million kWh, respectively, an increase of 238 million kWh or 64%, over
                            the quarter ended September 30, 2018, and an increase of 537 million kWh, or 69%, over the
                            six-months ended September 30, 2018. The increase in electricity generation was principally
                            a result of additional operating capacity during the period. The Company's Plant Load Factor
                            "PLF" for the quarter and the six months ended September 30, 2019, was 16.8% and 19.0%,
                            respectively, compared to 16.4 % and 18.1%, respectively, for the same comparable periods in
                            2018. The higher PLF, in the quarter and six months, is on account of additional capacity in
                            high radiation areas, which was offset by lower insolation due to extended monsoon (lowered
                            generation by approximately 3% year-over-year).</p>
                        <p>Revenue for the quarter ended September 30, 2019 was INR 2,846.6 million (<money>US$ 40.3
                                million</money>), an increase of 28% over the quarter ended September 30, 2018. Revenue
                            during the six months ended September 30, 2019 was INR 6,235.9 million (<money>US$ 88.3
                                million</money>), an increase of 34% from INR 4,648.2 million during the same period in
                            2018. The increase in revenue was primarily driven by the commissioning of new projects.</p>
                        <p>Project cost per megawatt operating (megawatt capacity per the Power Purchase Agreement
                            "PPA") consists of costs incurred for one megawatt of new solar power plant capacity during
                            the reporting period. The project cost per megawatt (DC) operating for the six months ended
                            September 30, 2019 decreased by INR 9.1 million (<money>US$ 0.13 million</money>) to INR
                            35.3 million (<money>US$ 0.50 million</money>) primarily due to lower costs on account of
                            the reduction in solar module prices for the projects commissioned during the period. The
                            project cost per megawatt (AC) operating for the six months ended September 30, 2019 was INR
                            51.9 million (<money>US$ 0.73 million</money>), compared to INR 50.8 million, for the six
                            months ended September 30, 2018, on account of additional safeguard duties paid by the
                            Company, which was partially offset by a reduction in solar module prices.. Excluding the
                            impact of safeguard duties, the DC and the AC costs per megawatt would have been lower by
                            approximately INR 2.4 million (<money>US$ 0.04 million</money>) and INR 3.6 million (<money>
                                US$ 0.05 million</money>), respectively.</p>
                        <p>As of September 30, 2019, the Company's operating and committed megawatts increased by 311 MW
                            to 3,370 MW compared to September 30, 2018. During the period, the Company's cancelled its
                            participation in 350 MWs of projects, and received letter of awards for 370 MWs of new
                            projects. </p>
