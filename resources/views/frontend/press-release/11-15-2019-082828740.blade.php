                        <p>Adjusted EBITDA was INR 2,078.5 million (<money>US$ 29.4 million</money>) for the quarter
                            ended September 30, 2019, compared to INR 1,807.7 million for the quarter ended September
                            30, 2018. The increase was primarily due to the increase in revenue during the quarter ended
                            September 30, 2019, partially offset by higher expenses related to operations and general
                            and administrative expenses.</p>
