                            which was subsequently utilised to pay existing debt and to fund capital expenditures. Cash
                            generated from financing activities for the quarter ended September 30, 2019 was INR 19,023
                            million (<money>US$ 269.3 million</money>) compared to INR 4,205.5 million in the prior
                            comparable period in 2018. </p>
