                        <p><b>Use of Non-GAAP Financial Measures</b></p>
                        <p><b><i>Adjusted EBITDA</i></b></p>
                        <p>Adjusted EBITDA is a non-GAAP financial measure. The Company presents Adjusted EBITDA as a
                            supplemental measure of its performance. This measurement is not recognized in accordance
                            with <location value="LC/us" idsrc="xmltag.org">U.S.</location> GAAP and should not be
                            viewed as an alternative to <location value="LC/us" idsrc="xmltag.org">U.S.</location>
                            GAAP measures of performance. The presentation of Adjusted EBITDA should not be construed as
                            an inference that the Company's future results will be unaffected by unusual or
                            non-recurring items.</p>
                        <p>The Company defines Adjusted EBITDA as net loss (income) plus (a) income tax expense, (b)
                            interest expense, net, (c) depreciation and amortization and (d) loss (income) on foreign
                            currency exchange. The Company believes Adjusted EBITDA is useful to investors in assessing
                            the Company's ongoing financial performance and provides improved comparability between
                            periods through the exclusion of certain items that management believes are not indicative
                            of the Company's operational profitability and that may obscure underlying business results
                            and trends. However, this measure should not be considered in isolation or viewed as a
                            substitute for net income or other measures of performance determined in accordance with
                            <location value="LC/us" idsrc="xmltag.org">U.S.</location> GAAP. Moreover, Adjusted
                            EBITDA as used herein is not necessarily comparable to other similarly titled measures of
                            other companies due to potential inconsistencies in the methods of calculation.</p>
                        <p>The Company's management believes this measure is useful to compare general operating
                            performance from period to period and to make certain related management decisions. Adjusted
                            EBITDA is also used by securities analysts, lenders and others in their evaluation of
                            different companies because it excludes certain items that can vary widely across different
                            industries or among companies within the same industry. For example, interest expense can be
                            highly dependent on a company's capital structure, debt levels and credit ratings.
                            Therefore, the impact of interest expense on earnings can vary significantly among
                            companies. In addition, the tax positions of companies can vary because of their differing
                            abilities to take advantage of tax benefits and because of the tax policies of the various
                            jurisdictions in which they operate. As a result, effective tax rates and tax expense can
                            vary considerably among companies.</p>
                        <p>Adjusted EBITDA has limitations as an analytical tool, and you should not consider it in
                            isolation or as a substitute for analysis of the Company's results as reported under
                            <location value="LC/us" idsrc="xmltag.org">U.S.</location> GAAP. Some of these
                            limitations include:</p>
                        <ul type="disc">
                            <li>it does not reflect cash expenditures or future requirements for capital expenditures or
                                contractual commitments or foreign exchange gain/loss; </li>
                            <li>it does not reflect changes in, or cash requirements for, working capital; </li>
                            <li>it does not reflect significant interest expense or the cash requirements necessary to
                                service interest or principal payments on outstanding debt; </li>
                            <li>it does not reflect payments made or future requirements for income taxes; and </li>
                            <li>although depreciation and amortization are non-cash charges, the assets being
                                depreciated and amortized will often have to be replaced or paid in the future and
                                Adjusted EBITDA does not reflect cash requirements for such replacements or payments.
                            </li>
                        </ul>
