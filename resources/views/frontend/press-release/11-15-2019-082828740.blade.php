                        <p><b>Fiscal Second Quarter 2020 Period ended September 30, 2019 Consolidated Financial
                                Results:</b></p>
                        <p><b><i>Operating Revenues</i></b></p>
                        <p>Operating revenues for the quarter ended September 30, 2019 was INR 2,846.6 million (<money>
                                US$ 40.3 million</money>), an increase of 28% from INR 2,225.7 million in the same
                            period in 2018. The increase was driven by the commissioning of new projects during the
                            period after September 30, 2018 until September 30, 2019.</p>
                        <p><b><i>Cost of Operations (Exclusive of Depreciation and Amortization)</i></b></p>
                        <p>Cost of operations for the quarter ended September 30, 2019 increased by 44% to INR 253.7
                            million (<money>US$ 3.6 million</money>) from INR 176.1 million in the same period in 2018.
                            The increase was primarily due to higher lease, solar park and land development expenses
                            from newly commissioned projects during the period after September 30, 2018 until September
                            30, 2019. </p>
                        <p><b><i>General and Administrative Expenses</i></b></p>
                        <p>General and administrative expenses for the quarter ended September 30, 2019 increased by INR
                            272.5 million (<money>US$ 3.9 million</money>), to INR 514.4 million (<money>US$ 7.3 million
                            </money>) compared to the same period in 2018. INR 148.0 million (<money>US$ 2.1 million
                            </money>) of the increase in general and administrative expense was primarily on account of
                            non-cash impact of leases, provision related account receivable, and write back of certain
                            provisions in the prior period.</p>
                        <p>In the quarter ended September 30, 2019, the Company recorded expenses of INR 7.5 million (
                            <money>US$ 0.1 million</money>), related to the grant of stock appreciation rights to its
                            CEO and president and INR 0.5 million (<money>US$ 0.01 million</money>), related to the
                            grant of restricted stock units to two of its independent directors.</p>
                        <p><b><i>Depreciation and Amortization
                                    Expenses&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
                                </i></b></p>
                        <p>Depreciation and amortization expenses during the quarter ended September 30, 2019 increased
                            by INR 72.8 million (<money>US$ 1.0 million</money>), or 12.2%, to INR 670.3 million (
                            <money>US$ 9.5 million</money>) compared to the same period in 2018. The increase in
                            depreciation and amortization expense reflected the additional depreciation on new projects
                            commissioned since September 30, 2018, partially offset by a decrease in depreciation
                            expense on account of change in useful life, which took effect from October 1, 2018.</p>
                        <p><b><i>Interest Expense, Net</i></b></p>
                        <p>Net interest expense during the quarter ended September 30, 2019 increased by INR 665.7
                            million (<money>US$ 9.4 million</money>), or 53.0%, to INR 1,922.8 million (<money>US$ 27.2
                                million</money>) compared to the same period in 2018. The increase in net interest
                            expense was primarily due to lower interest income of INR 138.4 million (<money>US$ 2.0
                                million</money>), and higher interest expense on account of additional borrowings
                            related to the commissioned projects since the quarter ended September 30, 2018. </p>
                        <p>The Company expects to incur charges of approximately INR 390.0 million (<money>~US$ 5.4
                                million</money>), related to write-offs and prepayment charges related to issuance of
                            solar green bond during the third fiscal quarter 2020 period ending December 31, 2019.</p>
                        <p><b><i>Loss on Foreign Currency Exchange</i></b></p>
                        <p>The Indian Rupee ("INR") depreciated against the <location value="LC/us"
                                idsrc="xmltag.org">U.S.</location> dollar by INR 1.72 for every <money>US$ 1.00</money>
                            (or 2.4%) during the period from June 30, 2019 to September 30, 2019. During the quarter
                            ended September 30, 2019, the Company incurred a loss on foreign exchange of INR 214.7
                            million (<money>US$ 3.0 million</money>) compared to a loss of INR 236.8 million, during the
                            quarter ended September 30, 2018. The Company had lower losses in foreign exchange primarily
                            due to lower depreciation of INR against the <location value="LC/us" idsrc="xmltag.org">
                                U.S.</location> dollar, compared to the prior comparable period, partially offset by
                            higher expense on hedging and derivatives transaction.</p>
                        <p><b><i>Income Tax Expense</i></b></p>
                        <p>Income tax expense increased during the quarter ended September 30, 2019 by INR 13.1 million
                            (<money>US$ 0.2 million</money>) to INR 27.0 million (<money>US$ 0.4 million</money>),
                            compared to income tax expense of INR 13.9 million in the same period in 2018, primarily as
                            a result of lower deferred tax during the quarter. </p>
                        <p>On September 20, 2019, the President of <location value="LC/in" idsrc="xmltag.org">India
                            </location> issued The Taxation Laws (Amendment) Ordinance, 2019 ("Ordinance") which amends
                            the Income Tax Act, 1961 (ITA) and the Finance (No. 2) Act, 2019. The Ordinance provides for
                            reduction in corporate tax rates as well as minimum alternate tax rates; the Company is
                            evaluating the impact on the amendment on the income tax expense.</p>
                        <p><b><i>Net Loss</i></b></p>
                        <p>The net loss for the quarter ended September 30, 2019 increased by INR 458.6 million (<money>
                                US$ 6.5 million</money>) to INR 756.2 million (<money>US$ 10.7 million</money>),
                            compared to a net loss of INR 297.6 million for the same period in 2018. The higher losses
                            are primarily due to higher general and administrative expenses, higher net interest
                            expenses, partially offset by higher revenue.</p>
