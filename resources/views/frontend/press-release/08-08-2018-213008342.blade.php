                        <p><b>First Quarter 2019 Period ended June 30, 2018 Consolidated Financial
                                Results:</b></p>
                        <p><i><b>Operating Revenue</b></i></p>
                        <p>
                            Operating revenue in the quarter ended June 30, 2018 was INR 2,422.5
                            million (<money>US$35.4 million</money>), an increase of 29% from INR 1,877.9 million
                            over the same period in 2017. The increase in revenue was driven by the
                            commissioning of new projects.
                        </p>
                        <p>
                            In May 2014, the FASB issued revised accounting guidance for revenue
                            recognition from contracts with customers. The Company adopted this
                            revised accounting guidance for interim and annual reporting periods
                            beginning April 1, 2018 using the modified retrospective method. Upon
                            adoption of the standard the Company recorded a cumulative-effect of a
                            change in accounting principle as of April 1, 2018 to reduce its
                            accumulated deficit by INR 218.4 million (<money>US$3.2 million</money>), related to
                            changes in the timing of revenue recognition for certain contracts with
                            its customers.
                        </p>
                        <p><i><b>Cost of Operations</b></i></p>
                        <p>
                            Cost of operations in the quarter ended June 30, 2018 increased by 26%
                            to INR 218.2 million (<money>US$3.2 million</money>) from INR 173.5 million in the same
                            period in 2017. The increase was primarily due to plant maintenance cost
                            for newly commissioned projects.
                        </p>
                        <p><i><b>General and Administrative Expenses</b></i></p>
                        <p>
                            General and administrative expenses for the quarter ended June 30, 2018
                            increased by INR 13.6 million (<money>US$0.02 million</money>), to INR 248.7 million
                            (<money>US$3.6 million</money>) compared to the same period in 2017. The general and
                            administrative expenses did not increase significantly due to scale of
                            operations during the three months ended June 30, 2018, as compared to
                            previous comparable quarter in 2017.
                        </p>
                        <p><i><b>Depreciation and Amortization Expenses</b></i></p>
                        <p>
                            Depreciation and amortization expenses during the quarter ended June 30,
                            2018 increased by INR 133.9 million (<money>US$2.0 million</money>), or 32%, to INR
                            553.6 million (<money>US$8.1 million</money>) compared to the same period in 2017. The
                            principal reason for the increase was capitalization of new projects
                            during the period from June 30, 2017 to June 30, 2018.
                        </p>
                        <p><i><b>Interest Expense, Net</b></i></p>
                        <p>
                            Net interest expense during the quarter ended June 30, 2018 increased by
                            INR 233.8 million (<money>US$3.4 million</money>), or 28%, to INR 1,073.4 million
                            (<money>US$15.7 million</money>) compared to the same period in 2017. Interest expense
                            increased on account of borrowings for new projects and was partially
                            offset by the increased interest income on investments during the
                            quarter ended June 30, 2018.
                        </p>
                        <p><i><b>Gain / Loss on Foreign Currency Exchange</b></i></p>
                        <p>
                            The Indian rupee depreciated against the <location value="LC/us" idsrc="xmltag.org">U.S.
                            </location> dollar by INR 3.6 to <money>US$
                                1.00</money> (5.5%) during the period from March 31, 2018 to June 30, 2018. This
                            depreciation during the period from March 31, 2018 to June 30, 2018
                            resulted in a foreign exchange loss of INR 204.2 million (<money>US$3.0
                                million</money>), compared to a marginal gain of INR 4.8 million during the same
                            period in 2017.
                        </p>
                        <p><i><b>Income Tax Expense / Benefit</b></i></p>
                        <p>
                            The income tax expense increased during the quarter ended June 30, 2018
                            by INR 86.7 million (<money>US$1.3 million</money>) to INR 94.6 million (<money>US$1.4
                                million</money>), compared to income tax expense of INR 7.9 million in the same
                            period in 2017.
                        </p>
                        <p><i><b>Net Income/ Loss</b></i></p>
                        <p>
                            The net income for the quarter ended June 30, 2018 was INR 29.8 million
                            (<money>US$0.4 million</money>), as compared to a net income of INR 206.9 million for
                            the quarter ended June 30, 2017, a decrease in net income by INR 177.1
                            million (<money>US$2.6 million</money>) as compared to the same period in 2017. This
                            was primarily due to foreign exchange loss related to the depreciation
                            of the Indian Rupee compared to the <location value="LC/us" idsrc="xmltag.org">U.S.
                            </location> Dollar during the quarter ended
                            June 30, 2018.
                        </p>
                        <p><i><b>Cash Flow and Working Capital</b></i></p>
                        <p>
                            Cash used in operating activities for the fiscal quarter ended June 30,
                            2018 was INR 749.6 million (<money>US$11.0 million</money>), INR 1,171.0 million
                            (<money>US$17.1 million</money>) lower than the prior comparable period, primarily due
                            to the repayment of INR 1,412.0 million (<money>US$20.6 million</money>) of interest
                            accrued since August 2017 on the Solar Green Bonds during the current
                            period.
                        </p>
                        <p>
                            Cash used in investing activities, for the fiscal quarter ended June 30,
                            2018 was INR 4,196.0 million (<money>US$61.3 million</money>), compared to INR 3,316.9
                            million for the prior comparable period, primarily on account of
                            purchases of property plant and equipment for new projects amounting to
                            INR 2,500.0 million (<money>US$36.5 million</money>) and an increase in cash available
                            for future projects invested in short term investments for INR 1,705.6
                            million (<money>US$24.9 million</money>).
                        </p>
                        <p>
                            Cash generated from financing activities was INR 4,251.7 (<money>US$62.1
                                million</money>) for the fiscal quarter ended June 30, 2018, compared to INR
                            2,460.3 million for the prior comparable period.
                        </p>
                        <p><i><b>Liquidity Position</b></i></p>
                        <p>
                            As of June 30, 2018, the Company had INR 10,831.3 million (<money>US$158.2
                                million</money>) of cash, cash equivalents and current investments. The Company
                            had undrawn project debt commitments of INR 12,629.9 million (<money>US$184.5
                                million</money>) as of June 30, 2018.
                        </p>
                        <p><i><b>Adjusted EBITDA</b></i></p>
                        <p>
                            Adjusted EBITDA was INR 1,955.7 million (<money>US$28.6 million</money>) for the fiscal
                            first quarter period ended 2019, compared to INR 1,469.3 million in the
                            first quarter ended June 30, 2017. The increase was primarily due to the
                            increase in revenue and economies of scale achieved during the period.
                        </p>
